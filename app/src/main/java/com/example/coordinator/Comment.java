package com.example.coordinator;

public class Comment {
    private String text;
    private String date;

    public Comment() {
        //For firestore
    }

    public Comment(String text, String date) {
        this.text = text;
        this.date = date;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
